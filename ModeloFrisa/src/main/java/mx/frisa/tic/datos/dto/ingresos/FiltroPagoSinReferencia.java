package mx.frisa.tic.datos.dto.ingresos;

public class FiltroPagoSinReferencia {
    private String cuentaBancaria;
    private String fechaInicial;
    private String fechaFinal;
    private String mostrarAplicar;
    private String usuario;
    

    public String getCuentaBancaria() {
        return cuentaBancaria;
    }

    public void setCuentaBancaria(String cuentaBancaria) {
        this.cuentaBancaria = cuentaBancaria;
    }

    public String getFechaInicial() {
        return fechaInicial;
    }

    public void setFechaInicial(String fechaInicial) {
        this.fechaInicial = fechaInicial;
    }

    public String getFechaFinal() {
        return fechaFinal;
    }

    public void setFechaFinal(String fechaFinal) {
        this.fechaFinal = fechaFinal;
    }

    public String getMostrarAplicar() {
        return mostrarAplicar;
    }

    public void setMostrarAplicar(String mostrarAplicar) {
        this.mostrarAplicar = mostrarAplicar;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }
    
    
    
    
}
